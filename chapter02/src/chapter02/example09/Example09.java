package chapter02.example09;

public class Example09 {
    public static void main(String[] args) {
        int grade = 75;
        if (grade > 80) {
            System.out.println("成绩等级为A");
        } else if (grade > 70) {
            System.out.println("成绩等级为B");
        } else if (grade > 60) {
            System.out.println("成绩等级为C");
        } else {
            System.out.println("成绩等级为D");
        }
    }
}
